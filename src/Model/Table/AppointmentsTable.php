<?php
namespace App\Model\Table;

use Cake\ORM\Query;
use Cake\ORM\RulesChecker;
use Cake\ORM\Table;
use Cake\Validation\Validator;

/**
 * Appointments Model
 *
 * @property \App\Model\Table\UsersTable&\Cake\ORM\Association\BelongsTo $Users
 *
 * @method \App\Model\Entity\Appointment get($primaryKey, $options = [])
 * @method \App\Model\Entity\Appointment newEntity($data = null, array $options = [])
 * @method \App\Model\Entity\Appointment[] newEntities(array $data, array $options = [])
 * @method \App\Model\Entity\Appointment|false save(\Cake\Datasource\EntityInterface $entity, $options = [])
 * @method \App\Model\Entity\Appointment saveOrFail(\Cake\Datasource\EntityInterface $entity, $options = [])
 * @method \App\Model\Entity\Appointment patchEntity(\Cake\Datasource\EntityInterface $entity, array $data, array $options = [])
 * @method \App\Model\Entity\Appointment[] patchEntities($entities, array $data, array $options = [])
 * @method \App\Model\Entity\Appointment findOrCreate($search, callable $callback = null, $options = [])
 */
class AppointmentsTable extends Table
{
    /**
     * Initialize method
     *
     * @param array $config The configuration for the Table.
     * @return void
     */
    public function initialize(array $config)
    {
        parent::initialize($config);

        $this->setTable('appointments');
        $this->setDisplayField('id');
        $this->setPrimaryKey('id');

        $this->belongsTo('Users', [
            'foreignKey' => 'user_id',
            'joinType' => 'INNER',
        ]);
    }

    /**
     * Default validation rules.
     *
     * @param \Cake\Validation\Validator $validator Validator instance.
     * @return \Cake\Validation\Validator
     */
    public function validationDefault(Validator $validator)
    {
        $validator
            ->integer('id')
            ->allowEmptyString('id', null, 'create');

        $validator
            ->scalar('beauty_care_service')
            ->maxLength('beauty_care_service', 255)
            ->requirePresence('beauty_care_service', 'create')
            ->notEmptyString('beauty_care_service');

        $validator
            ->scalar('appointment_name')
            ->maxLength('appointment_name', 255)
            ->requirePresence('appointment_name', 'create')
            ->notEmptyString('appointment_name');

        $validator
            ->scalar('appointment_phone')
            ->maxLength('appointment_phone', 255)
            ->allowEmptyString('appointment_phone');

        $validator
            ->scalar('appointment_email')
            ->maxLength('appointment_email', 255)
            ->allowEmptyString('appointment_email');

        $validator
            ->dateTime('appointment_datetime')
            ->requirePresence('appointment_datetime', 'create')
            ->notEmptyDateTime('appointment_datetime');

        $validator
            ->scalar('appointment_comment')
            ->maxLength('appointment_comment', 255)
            ->allowEmptyString('appointment_comment');

        $validator
            ->scalar('appointment_address')
            ->maxLength('appointment_address', 255)
            ->allowEmptyString('appointment_address');

        return $validator;
    }

    /**
     * Returns a rules checker object that will be used for validating
     * application integrity.
     *
     * @param \Cake\ORM\RulesChecker $rules The rules object to be modified.
     * @return \Cake\ORM\RulesChecker
     */
    public function buildRules(RulesChecker $rules)
    {
        $rules->add($rules->existsIn(['user_id'], 'Users'));

        return $rules;
    }
}
